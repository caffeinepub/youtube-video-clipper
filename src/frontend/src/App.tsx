import { StrictMode, useState, useEffect } from 'react';
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { RouterProvider, createRouter, createRoute, createRootRoute, Outlet } from '@tanstack/react-router';
import { Toaster } from '@/components/ui/sonner';
import Layout from './components/Layout';
import VideoUrlForm from './components/VideoUrlForm';
import YouTubePlayer from './components/YouTubePlayer';
import ClipTimestampControls from './components/ClipTimestampControls';
import ClipList from './components/ClipList';
import ClipSuggestions from './components/ClipSuggestions';
import AdminPanel from './components/AdminPanel';
import AdminErrorBoundary from './components/AdminErrorBoundary';
import { useIsOwner } from './hooks/useIsOwner';
import { useInternetIdentity } from './hooks/useInternetIdentity';
import { Loader2, AlertCircle } from 'lucide-react';
import type { VideoClip, AutoGeneratedClipSuggestion } from './backend';

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      retry: 1,
    },
  },
});

function HomePage() {
  const [videoId, setVideoId] = useState<string>('');
  const [videoUrl, setVideoUrl] = useState<string>('');
  const [selectedClip, setSelectedClip] = useState<VideoClip | null>(null);
  const [suggestedClip, setSuggestedClip] = useState<{
    startTime: number;
    endTime: number;
    title: string;
  } | null>(null);
  const [aiGeneratedSuggestions, setAiGeneratedSuggestions] = useState<AutoGeneratedClipSuggestion[]>([]);

  const handleVideoSubmit = (url: string, id: string) => {
    setVideoUrl(url);
    setVideoId(id);
    setSelectedClip(null);
    setSuggestedClip(null);
    setAiGeneratedSuggestions([]);
  };

  const handleClipsGenerated = (clips: AutoGeneratedClipSuggestion[]) => {
    setAiGeneratedSuggestions(clips);
    setSuggestedClip(null);
  };

  const handleClipSelect = (clip: VideoClip) => {
    setSelectedClip(clip);
    setSuggestedClip(null);
    // Extract video ID from the clip's video URL
    const urlMatch = clip.videoUrl.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([^&\s]+)/);
    if (urlMatch) {
      setVideoId(urlMatch[1]);
      setVideoUrl(clip.videoUrl);
    }
  };

  const handleSuggestionSelect = (startTime: number, endTime: number, title: string) => {
    setSuggestedClip({ startTime, endTime, title });
    setSelectedClip(null);
  };

  return (
    <div className="container mx-auto px-4 py-8">
      <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
        {/* Main Content Area */}
        <div className="lg:col-span-2 space-y-6">
          <VideoUrlForm 
            onSubmit={handleVideoSubmit}
            onClipsGenerated={handleClipsGenerated}
          />
          
          {videoId && (
            <>
              <YouTubePlayer videoId={videoId} />
              <ClipTimestampControls 
                videoUrl={videoUrl}
                videoId={videoId}
                selectedClip={selectedClip}
                suggestedStartTime={suggestedClip?.startTime}
                suggestedEndTime={suggestedClip?.endTime}
                suggestedTitle={suggestedClip?.title}
              />
            </>
          )}
        </div>

        {/* Sidebar */}
        <div className="lg:col-span-1 space-y-6">
          <ClipSuggestions 
            onSelectSuggestion={handleSuggestionSelect}
            aiSuggestions={aiGeneratedSuggestions}
          />
          <ClipList onClipSelect={handleClipSelect} selectedClipId={selectedClip?.id} />
        </div>
      </div>
    </div>
  );
}

function AdminRoute() {
  const { identity, isInitializing } = useInternetIdentity();
  const { 
    isOwner, 
    isLoading, 
    isFetched, 
    error, 
    isError,
    rawData,
    queryStatus,
    actorAvailable 
  } = useIsOwner();
  
  const principalId = identity?.getPrincipal().toString() || 'Not authenticated';
  
  // Log on mount
  useEffect(() => {
    console.log('═══════════════════════════════════════════════════════');
    console.log('[AdminRoute] 🎬 Component MOUNTED at', new Date().toISOString());
    console.log('[AdminRoute] Initial mount state:', {
      isAuthenticated: !!identity,
      principalId,
      isInitializing,
    });
    console.log('═══════════════════════════════════════════════════════');
  }, []);

  // Log on every render with full state
  useEffect(() => {
    console.log('───────────────────────────────────────────────────────');
    console.log('[AdminRoute] 🔄 RENDER at', new Date().toISOString());
    console.log('[AdminRoute] Authentication:', {
      isAuthenticated: !!identity,
      principalId,
      isInitializing,
    });
    console.log('[AdminRoute] useIsOwner hook returned:', {
      isOwner,
      isOwnerType: typeof isOwner,
      isOwnerValue: isOwner === true ? 'TRUE' : isOwner === false ? 'FALSE' : 'OTHER',
      rawData,
      rawDataType: typeof rawData,
      isLoading,
      isFetched,
      isError,
      queryStatus,
      actorAvailable,
      error: error ? String(error) : null,
    });
    console.log('[AdminRoute] Conditional logic evaluation:');
    console.log('  - (!identity || isLoading || isInitializing):', !identity || isLoading || isInitializing);
    console.log('  - (isFetched && !isOwner):', isFetched && !isOwner);
    console.log('  - Will render:', 
      !identity || isLoading || isInitializing ? 'LOADING SCREEN' : 
      isFetched && !isOwner ? 'ACCESS DENIED SCREEN' : 
      'ADMIN PANEL'
    );
    console.log('───────────────────────────────────────────────────────');
  });

  // Show loading while checking authentication or initializing
  if (!identity || isLoading || isInitializing) {
    console.log('[AdminRoute] ⏳ Rendering LOADING state');
    console.log('[AdminRoute] Reason:', !identity ? 'No identity' : isInitializing ? 'Initializing' : 'isLoading=true');
    return (
      <div className="min-h-screen flex items-center justify-center bg-background">
        <div className="text-center space-y-4">
          <Loader2 className="h-8 w-8 animate-spin mx-auto text-primary" />
          <p className="text-muted-foreground">
            {isInitializing ? 'Initializing authentication...' : 'Checking admin access...'}
          </p>
        </div>
      </div>
    );
  }

  // Show access denied if not owner
  if (isFetched && !isOwner) {
    console.log('[AdminRoute] ❌ Rendering ACCESS DENIED screen');
    console.log('[AdminRoute] Reason: isFetched=true AND isOwner=false');
    
    return (
      <div className="min-h-screen flex items-center justify-center bg-background p-4">
        <div className="max-w-2xl w-full space-y-6">
          <div className="text-center space-y-2">
            <AlertCircle className="h-12 w-12 mx-auto text-destructive" />
            <h1 className="text-2xl font-bold text-foreground">Access Denied</h1>
            <p className="text-muted-foreground">
              You don't have permission to access the admin panel.
            </p>
          </div>
          
          <div className="space-y-4">
            {/* Principal ID */}
            <div className="p-4 bg-muted rounded-lg">
              <p className="text-sm font-medium text-foreground mb-2">Your Principal ID:</p>
              <code className="text-xs break-all bg-background p-3 rounded block font-mono">
                {principalId}
              </code>
            </div>

            {/* Debug Information */}
            <div className="p-4 bg-muted rounded-lg space-y-3">
              <p className="text-sm font-medium text-foreground">Debug Information:</p>
              
              <div className="space-y-2 text-xs font-mono">
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">isOwner (hook):</span>
                  <span className={isOwner ? 'text-green-600 font-bold' : 'text-red-600 font-bold'}>
                    {String(isOwner)}
                  </span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Raw Data:</span>
                  <span className="text-foreground font-bold">
                    {rawData === undefined ? 'undefined' : String(rawData)}
                  </span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Query Status:</span>
                  <span className="text-foreground">{queryStatus}</span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Actor Available:</span>
                  <span className={actorAvailable ? 'text-green-600' : 'text-red-600'}>
                    {String(actorAvailable)}
                  </span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Is Fetched:</span>
                  <span className={isFetched ? 'text-green-600' : 'text-yellow-600'}>
                    {String(isFetched)}
                  </span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Is Loading:</span>
                  <span className={isLoading ? 'text-yellow-600' : 'text-green-600'}>
                    {String(isLoading)}
                  </span>
                </div>
                
                <div className="flex justify-between p-2 bg-background rounded">
                  <span className="text-muted-foreground">Has Error:</span>
                  <span className={isError ? 'text-red-600' : 'text-green-600'}>
                    {String(isError)}
                  </span>
                </div>
                
                {error && (
                  <div className="p-2 bg-destructive/10 border border-destructive/20 rounded">
                    <p className="text-destructive font-medium mb-1">Error Details:</p>
                    <pre className="text-destructive text-xs whitespace-pre-wrap break-all">
                      {error instanceof Error ? error.message : String(error)}
                    </pre>
                  </div>
                )}
              </div>
            </div>

            {/* Backend Call Status */}
            <div className="p-4 bg-muted rounded-lg">
              <p className="text-sm font-medium text-foreground mb-2">Backend Call Status:</p>
              <div className="text-xs space-y-1">
                {!actorAvailable && (
                  <p className="text-destructive">⚠️ Backend actor not initialized</p>
                )}
                {actorAvailable && !isFetched && (
                  <p className="text-yellow-600">⏳ Backend call pending...</p>
                )}
                {actorAvailable && isFetched && !isError && (
                  <p className="text-green-600">✓ Backend call completed successfully</p>
                )}
                {isError && (
                  <p className="text-destructive">✗ Backend call failed</p>
                )}
              </div>
            </div>
          </div>
          
          <div className="text-center">
            <a
              href="/"
              className="inline-block px-6 py-2 bg-primary text-primary-foreground rounded-lg hover:bg-primary/90 transition-colors"
            >
              Return to Home
            </a>
          </div>
        </div>
      </div>
    );
  }

  // Show admin panel if owner
  console.log('[AdminRoute] ✅ Rendering ADMIN PANEL');
  console.log('[AdminRoute] Reason: isFetched=true AND isOwner=true');
  return (
    <AdminErrorBoundary>
      <AdminPanel />
    </AdminErrorBoundary>
  );
}

const rootRoute = createRootRoute({
  component: () => (
    <Layout>
      <Outlet />
    </Layout>
  ),
});

const indexRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/',
  component: HomePage,
});

const adminRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/admin',
  component: AdminRoute,
});

const routeTree = rootRoute.addChildren([indexRoute, adminRoute]);

const router = createRouter({ routeTree });

declare module '@tanstack/react-router' {
  interface Register {
    router: typeof router;
  }
}

export default function App() {
  return (
    <StrictMode>
      <QueryClientProvider client={queryClient}>
        <RouterProvider router={router} />
        <Toaster />
      </QueryClientProvider>
    </StrictMode>
  );
}
