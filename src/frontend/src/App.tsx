import { StrictMode, useState } from 'react';
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { RouterProvider, createRouter, createRoute, createRootRoute, Outlet } from '@tanstack/react-router';
import { Toaster } from '@/components/ui/sonner';
import Layout from './components/Layout';
import VideoUrlForm from './components/VideoUrlForm';
import YouTubePlayer from './components/YouTubePlayer';
import ClipTimestampControls from './components/ClipTimestampControls';
import ClipList from './components/ClipList';
import ClipSuggestions from './components/ClipSuggestions';
import AdminPanel from './components/AdminPanel';
import AdminErrorBoundary from './components/AdminErrorBoundary';
import { useAdminPasswordAuth } from './hooks/useAdminPasswordAuth';
import { useInternetIdentity } from './hooks/useInternetIdentity';
import { Loader2, AlertCircle, Lock } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import type { VideoClip, AutoGeneratedClipSuggestion } from './backend';

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      retry: 1,
    },
  },
});

function HomePage() {
  const [videoId, setVideoId] = useState<string>('');
  const [videoUrl, setVideoUrl] = useState<string>('');
  const [selectedClip, setSelectedClip] = useState<VideoClip | null>(null);
  const [suggestedClip, setSuggestedClip] = useState<{
    startTime: number;
    endTime: number;
    title: string;
  } | null>(null);
  const [aiGeneratedSuggestions, setAiGeneratedSuggestions] = useState<AutoGeneratedClipSuggestion[]>([]);

  const handleVideoSubmit = (url: string, id: string) => {
    setVideoUrl(url);
    setVideoId(id);
    setSelectedClip(null);
    setSuggestedClip(null);
    setAiGeneratedSuggestions([]);
  };

  const handleClipsGenerated = (clips: AutoGeneratedClipSuggestion[]) => {
    setAiGeneratedSuggestions(clips);
    setSuggestedClip(null);
  };

  const handleClipSelect = (clip: VideoClip) => {
    setSelectedClip(clip);
    setSuggestedClip(null);
    // Extract video ID from the clip's video URL
    const urlMatch = clip.videoUrl.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([^&\s]+)/);
    if (urlMatch) {
      setVideoId(urlMatch[1]);
      setVideoUrl(clip.videoUrl);
    }
  };

  const handleSuggestionSelect = (startTime: number, endTime: number, title: string) => {
    setSuggestedClip({ startTime, endTime, title });
    setSelectedClip(null);
  };

  return (
    <div className="container mx-auto px-4 py-8">
      <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
        {/* Main Content Area */}
        <div className="lg:col-span-2 space-y-6">
          <VideoUrlForm 
            onSubmit={handleVideoSubmit}
            onClipsGenerated={handleClipsGenerated}
          />
          
          {videoId && (
            <>
              <YouTubePlayer videoId={videoId} />
              <ClipTimestampControls 
                videoUrl={videoUrl}
                videoId={videoId}
                selectedClip={selectedClip}
                suggestedStartTime={suggestedClip?.startTime}
                suggestedEndTime={suggestedClip?.endTime}
                suggestedTitle={suggestedClip?.title}
              />
            </>
          )}
        </div>

        {/* Sidebar */}
        <div className="lg:col-span-1 space-y-6">
          <ClipSuggestions 
            onSelectSuggestion={handleSuggestionSelect}
            aiSuggestions={aiGeneratedSuggestions}
          />
          <ClipList onClipSelect={handleClipSelect} selectedClipId={selectedClip?.id} />
        </div>
      </div>
    </div>
  );
}

function AdminRoute() {
  const { identity, isInitializing } = useInternetIdentity();
  const { isValidated, validatePassword, isValidating, error } = useAdminPasswordAuth();
  const [password, setPassword] = useState('');

  // Show loading while initializing authentication
  if (isInitializing) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background">
        <div className="text-center space-y-4">
          <Loader2 className="h-8 w-8 animate-spin mx-auto text-primary" />
          <p className="text-muted-foreground">Initializing authentication...</p>
        </div>
      </div>
    );
  }

  // Require authentication
  if (!identity) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background p-4">
        <div className="max-w-md w-full text-center space-y-4">
          <AlertCircle className="h-12 w-12 mx-auto text-muted-foreground" />
          <h1 className="text-2xl font-bold text-foreground">Authentication Required</h1>
          <p className="text-muted-foreground">
            Please log in to access the admin panel.
          </p>
        </div>
      </div>
    );
  }

  // Show password prompt if not validated
  if (!isValidated) {
    const handleSubmit = async (e: React.FormEvent) => {
      e.preventDefault();
      await validatePassword(password);
    };

    return (
      <div className="min-h-screen flex items-center justify-center bg-background p-4">
        <div className="max-w-md w-full space-y-6">
          <div className="text-center space-y-2">
            <Lock className="h-12 w-12 mx-auto text-primary" />
            <h1 className="text-2xl font-bold text-foreground">Admin Access</h1>
            <p className="text-muted-foreground">
              Enter the admin password to access the admin panel.
            </p>
          </div>

          <form onSubmit={handleSubmit} className="space-y-4">
            <div className="space-y-2">
              <Input
                type="password"
                placeholder="Enter admin password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                disabled={isValidating}
                className="w-full"
                autoFocus
              />
              {error && (
                <p className="text-sm text-destructive flex items-center gap-2">
                  <AlertCircle className="h-4 w-4" />
                  {error}
                </p>
              )}
            </div>

            <Button
              type="submit"
              disabled={isValidating || !password.trim()}
              className="w-full"
            >
              {isValidating ? (
                <>
                  <Loader2 className="w-4 h-4 mr-2 animate-spin" />
                  Verifying...
                </>
              ) : (
                'Verify Password'
              )}
            </Button>
          </form>

          <div className="text-xs text-muted-foreground text-center">
            <p>Your password is verified securely with the backend.</p>
          </div>
        </div>
      </div>
    );
  }

  // Show admin panel if validated
  return (
    <AdminErrorBoundary>
      <AdminPanel />
    </AdminErrorBoundary>
  );
}

const rootRoute = createRootRoute({
  component: () => (
    <Layout>
      <Outlet />
    </Layout>
  ),
});

const indexRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/',
  component: HomePage,
});

const adminRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/admin',
  component: AdminRoute,
});

const routeTree = rootRoute.addChildren([indexRoute, adminRoute]);

const router = createRouter({ routeTree });

declare module '@tanstack/react-router' {
  interface Register {
    router: typeof router;
  }
}

export default function App() {
  return (
    <StrictMode>
      <QueryClientProvider client={queryClient}>
        <RouterProvider router={router} />
        <Toaster />
      </QueryClientProvider>
    </StrictMode>
  );
}
