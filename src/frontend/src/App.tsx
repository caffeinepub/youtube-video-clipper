import { StrictMode, useState } from 'react';
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { RouterProvider, createRouter, createRoute, createRootRoute, Outlet } from '@tanstack/react-router';
import { Toaster } from '@/components/ui/sonner';
import Layout from './components/Layout';
import VideoUrlForm from './components/VideoUrlForm';
import YouTubePlayer from './components/YouTubePlayer';
import ClipTimestampControls from './components/ClipTimestampControls';
import ClipList from './components/ClipList';
import ClipSuggestions from './components/ClipSuggestions';
import ChannelConnection from './components/ChannelConnection';
import AdminPanel from './components/AdminPanel';
import AdminErrorBoundary from './components/AdminErrorBoundary';
import ProfileSetup from './components/ProfileSetup';
import { useIsOwner } from './hooks/useIsOwner';
import { useInternetIdentity } from './hooks/useInternetIdentity';
import { useGetCallerUserProfile } from './hooks/useQueries';
import { Loader2, AlertCircle, ShieldOff } from 'lucide-react';
import type { VideoClip, AutoGeneratedClipSuggestion } from './backend';

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      retry: 1,
    },
  },
});

function HomePage() {
  const { identity, isInitializing } = useInternetIdentity();
  const { data: userProfile, isLoading: profileLoading, isFetched } = useGetCallerUserProfile();
  const [videoId, setVideoId] = useState<string>('');
  const [videoUrl, setVideoUrl] = useState<string>('');
  const [selectedClip, setSelectedClip] = useState<VideoClip | null>(null);
  const [suggestedClip, setSuggestedClip] = useState<{
    startTime: number;
    endTime: number;
    title: string;
  } | null>(null);
  const [aiGeneratedSuggestions, setAiGeneratedSuggestions] = useState<AutoGeneratedClipSuggestion[]>([]);

  const handleVideoSubmit = (url: string, id: string) => {
    setVideoUrl(url);
    setVideoId(id);
    setSelectedClip(null);
    setSuggestedClip(null);
    setAiGeneratedSuggestions([]);
  };

  const handleClipsGenerated = (clips: AutoGeneratedClipSuggestion[]) => {
    setAiGeneratedSuggestions(clips);
    setSuggestedClip(null);
  };

  const handleClipSelect = (clip: VideoClip) => {
    setSelectedClip(clip);
    setSuggestedClip(null);
    const urlMatch = clip.videoUrl.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([^&\s]+)/);
    if (urlMatch) {
      setVideoId(urlMatch[1]);
      setVideoUrl(clip.videoUrl);
    }
  };

  const handleSuggestionSelect = (startTime: number, endTime: number, title: string) => {
    setSuggestedClip({ startTime, endTime, title });
    setSelectedClip(null);
  };

  const isAuthenticated = !!identity;
  const showProfileSetup = isAuthenticated && !profileLoading && isFetched && userProfile === null;

  if (isInitializing || (isAuthenticated && profileLoading)) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background">
        <div className="text-center space-y-4">
          <Loader2 className="h-8 w-8 animate-spin mx-auto text-primary" />
          <p className="text-muted-foreground">Loading...</p>
        </div>
      </div>
    );
  }

  if (showProfileSetup) {
    return <ProfileSetup onComplete={() => {}} />;
  }

  return (
    <div className="container mx-auto px-4 py-8">
      <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
        <div className="lg:col-span-2 space-y-6">
          <VideoUrlForm 
            onSubmit={handleVideoSubmit}
            onClipsGenerated={handleClipsGenerated}
          />
          
          {isAuthenticated && <ChannelConnection />}
          
          {videoId && (
            <>
              <YouTubePlayer videoId={videoId} />
              <ClipTimestampControls 
                videoUrl={videoUrl}
                videoId={videoId}
                selectedClip={selectedClip}
                suggestedStartTime={suggestedClip?.startTime}
                suggestedEndTime={suggestedClip?.endTime}
                suggestedTitle={suggestedClip?.title}
              />
            </>
          )}
        </div>

        <div className="lg:col-span-1 space-y-6">
          <ClipSuggestions 
            onSelectSuggestion={handleSuggestionSelect}
            aiSuggestions={aiGeneratedSuggestions}
          />
          <ClipList onClipSelect={handleClipSelect} selectedClipId={selectedClip?.id} />
        </div>
      </div>
    </div>
  );
}

function AdminRoute() {
  const { identity, isInitializing } = useInternetIdentity();
  const { isOwner, isLoading: isCheckingOwner, isFetched } = useIsOwner();

  if (isInitializing || isCheckingOwner) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background">
        <div className="text-center space-y-4">
          <Loader2 className="h-8 w-8 animate-spin mx-auto text-primary" />
          <p className="text-muted-foreground">
            {isInitializing ? 'Initializing authentication...' : 'Verifying admin access...'}
          </p>
        </div>
      </div>
    );
  }

  if (!identity) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background p-4">
        <div className="max-w-md w-full text-center space-y-4">
          <AlertCircle className="h-12 w-12 mx-auto text-muted-foreground" />
          <h1 className="text-2xl font-bold text-foreground">Authentication Required</h1>
          <p className="text-muted-foreground">
            Please log in to access the admin panel.
          </p>
        </div>
      </div>
    );
  }

  if (isFetched && !isOwner) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-background p-4">
        <div className="max-w-md w-full text-center space-y-4">
          <ShieldOff className="h-12 w-12 mx-auto text-destructive" />
          <h1 className="text-2xl font-bold text-foreground">Access Denied</h1>
          <p className="text-muted-foreground">
            Only the email <strong>jj7250328@gmail.com</strong> has access to the admin panel.
          </p>
          <p className="text-sm text-muted-foreground">
            If this is your email, please make sure it's set correctly in your profile.
          </p>
        </div>
      </div>
    );
  }

  return (
    <AdminErrorBoundary>
      <AdminPanel />
    </AdminErrorBoundary>
  );
}

const rootRoute = createRootRoute({
  component: () => (
    <Layout>
      <Outlet />
    </Layout>
  ),
});

const indexRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/',
  component: HomePage,
});

const adminRoute = createRoute({
  getParentRoute: () => rootRoute,
  path: '/admin',
  component: AdminRoute,
});

const routeTree = rootRoute.addChildren([indexRoute, adminRoute]);

const router = createRouter({ routeTree });

declare module '@tanstack/react-router' {
  interface Register {
    router: typeof router;
  }
}

export default function App() {
  return (
    <StrictMode>
      <QueryClientProvider client={queryClient}>
        <RouterProvider router={router} />
        <Toaster />
      </QueryClientProvider>
    </StrictMode>
  );
}
